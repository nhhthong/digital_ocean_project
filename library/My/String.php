    public static function excerpt($string, $maxlength = 50)
    {
        $short_string = '';

        $string_tmp = strip_tags(htmlspecialchars_decode($string));
        $len = strlen($string_tmp);
        $short_string = substr($string_tmp, 0, $maxlength);
        $string_trim = substr( $short_string, 0, strrpos($short_string, " ") );

        $short_string = $len >  $maxlength ?  ($string_trim . " [...]") : $short_string;

        return $short_string;
    }

    public static function trim($str = '')
    {
        $str = strval($str);
        $str = str_replace('—', '-', $str);
        return trim(preg_replace(array('/\s{2,}/', '/[\t\n]+/'), ' ', $str));
    }

    public static function stringlistToArray($list)
    {
        $arr = trim($list);
        $arr = preg_replace("/(^[\r\n]*|[\r\n]+)[\s\t]*[\r\n]+/", "\n", $arr);
        $arr = explode("\n", $arr);
        $arr = array_filter($arr);

        return $arr;
    }
